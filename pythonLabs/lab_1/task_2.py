# Составить алгоритм решения ребуса КОТ + КОТ = ТОК
# (различные буквы означают различные цифры, старшая буква ‒ не 0).
#
for k in range(1,10):
    for t in range(0,10):
        for o in range(0,10):
            a = 100*k+10*t+o
            b = 100*k+10*o+t
            c = 100*t+10*o+k
            if a+b==c:
                print(str(a)+ " + " + str(b) + " = " + str(c))
